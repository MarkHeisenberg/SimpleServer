#ifndef __HTTP_TYPES_H__
#define __HTTP_TYPES_H__

#include <stdint.h>
#include <stddef.h>

typedef enum http_method {
    HTTP_METHOD_GET,
    HTTP_METHOD_POST,
    HTTP_METHOD_OPTIONS,
    HTTP_METHOD_PUT,
    HTTP_METHOD_PATCH,
    HTTP_METHOD_DELETE,
    HTTP_METHOD_HEAD,
    HTTP_METHOD_CONNECT,
    HTTP_METHOD_TRACE,
    HTTP_METHOD_COUNT //This must be always last in enumeration
} http_method_t;

typedef enum http_status {
    //Informative status codes
    HTTP_STATUS_CODE__CONTINUE = 100,
    HTTP_STATUS_CODE__SWITCHING_PROTOCOLS = 101,
    HTTP_STATUS_CODE__PROCESSING = 102,
    HTTP_STATUS_CODE__EARLY_HINTS = 103,

    //Successfull responce
    HTTP_STATUS_CODE__OK = 200,
    HTTP_STATUS_CODE__CREATED = 201,
    HTTP_STATUS_CODE__ACCEPTED = 202,
    HTTP_STATUS_CODE__NON_AUTHORITATIVE_INFORMATION = 203,
    HTTP_STATUS_CODE__NO_CONTENT = 204,
    HTTP_STATUS_CODE__RESET_CONTENT = 205,
    HTTP_STATUS_CODE__PARTIAL_CONTENT = 206,
    HTTP_STATUS_CODE__MULTI_STATUS = 207,
    HTTP_STATUS_CODE__ALREADY_REPORTED = 208,
    HTTP_STATUS_CODE__IM_USED = 226,

    //Redirection status codes
    HTTP_STATUS_CODE__MULTIPLE_CHOICES = 300,
    HTTP_STATUS_CODE__MOVED_PERMANENTLY = 301,
    HTTP_STATUS_CODE__FOUND = 302,
    HTTP_STATUS_CODE__SEE_OTHER = 303,
    HTTP_STATUS_CODE__NOT_MODIFIED = 304,
    HTTP_STATUS_CODE__USE_PROXY = 305,
    HTTP_STATUS_CODE__UNUSED = 306,
    HTTP_STATUS_CODE__TEMPORARY_REDIRECT = 307,
    HTTP_STATUS_CODE__PERMANENT_REDIRECT = 308,

    //Client error status codes
    HTTP_STATUS_CODE__BAD_REQUEST = 400,
    HTTP_STATUS_CODE__UNAUTHORIZED = 401,
    HTTP_STATUS_CODE__PAYMENT_REQUIRED = 402,
    HTTP_STATUS_CODE__FORBIDDEN = 403,
    HTTP_STATUS_CODE__NOT_FOUND = 404,
    HTTP_STATUS_CODE__METHOD_NOT_ALLOWED = 405,
    HTTP_STATUS_CODE__NOT_ACCEPTABLE = 406,
    HTTP_STATUS_CODE__REQUEST_TIMEOUT = 408,
    HTTP_STATUS_CODE__CONFLICT = 409,
    HTTP_STATUS_CODE__GONE = 410,
    HTTP_STATUS_CODE__LENGTH_REQUIRED = 411,
    HTTP_STATUS_CODE__PRECONDITION_FAILED = 412,
    HTTP_STATUS_CODE__PAYLOAD_TOO_LARGE = 413,
    HTTP_STATUS_CODE__URI_TOO_LONG = 414,
    HTTP_STATUS_CODE__UNSUPPORTED_MEDIA_TYPE = 415,
    HTTP_STATUS_CODE__RANGE_NOT_SATISFIABLE = 416,
    HTTP_STATUS_CODE__EXPECTATION_FAILED = 417,
    HTTP_STATUS_CODE__IM_A_TEAPOT = 418,
    HTTP_STATUS_CODE__MISDIRECTED_REQUEST = 421,
    HTTP_STATUS_CODE__UNPROCESSABLE_ENTITY = 422,
    HTTP_STATUS_CODE__LOCKED = 423,
    HTTP_STATUS_CODE__FAILED_DEPENDENCY = 424,
    HTTP_STATUS_CODE__TOO_EARLY = 425,
    HTTP_STATUS_CODE__UPGRADE_REQUIRED = 426,
    HTTP_STATUS_CODE__PRECONDOTION_REQUIRED = 428,
    HTTP_STATUS_CODE__TOO_MANY_REQUESTS = 429,
    HTTP_STATUS_CODE__REQUEST_HEADER_FIELDS_TOO_LARGE = 431,
    HTTP_STATUS_CODE__UNAVAILABLE_FOR_LEGAL_REASONS = 451,

    //Server error status codes
    HTTP_STATUS_CODE__INTERNAL_SERVER_ERROR = 500,
    HTTP_STATUS_CODE__NOT_IMPLEMENTED = 501,
    HTTP_STATUS_CODE__BAD_GATEWAY = 502,
    HTTP_STATUS_CODE__SERVICE_UNAVAILABLE = 503,
    HTTP_STATUS_CODE__GATEWAY_TIMEOUT = 504,
    HTTP_STATUS_CODE__HTTP_VERSION_NOT_SUPPORTED = 505,
    HTTP_STATUS_CODE__VARIANT_ALSO_NEGOTIATES = 506,
    HTTP_STATUS_CODE__INSUFFICIENT_STORAGE = 507,
    HTTP_STATUS_CODE__LOOP_DETECTED = 508,
    HTTP_STATUS_CODE__NOT_EXTENDED = 510,
    HTTP_STATUS_CODE__NETWORK_AUTHENTICATION_REQUIRED = 511,
    HTTP_STATUS_CODE__UNKNOWN
} http_status_t;

typedef struct http_request {
    http_method_t method;
    char* path;
    char* content_type;
    char* headers;
    char* body;
    size_t body_length;
} http_request_t;

typedef struct http_response {
    http_status_t status;
    char* headers;
    char* content_type;
    char* body;
    size_t body_length;
} http_response_t;

typedef int (http_handler_t)(http_request_t* request, http_response_t* response);

const char* http_status_string(http_status_t status);
const char* http_method_string(http_method_t method);
#endif // __HTTP_TYPES_H__